Constantes 

Definición: Una constante es un valor fijo que se asigna durante la declaración y que no puede cambiar mientras se ejecuta el programa.

Declaración: En (.k+), las constantes se declaran utilizando la palabra clave const. Por ejemplo, const PI = 3.14159; define una constante llamada PI con un valor fijo de 3.14159.

Características:

Una vez que se asigna un valor a una constante, no se puede modificar posteriormente.

Las constantes suelen usarse para representar valores que no deben cambiar, como valores matemáticos como PI o configuraciones predefinidas.

Ventajas:

Proporcionan claridad en el código al indicar valores que no deben modificarse. 

Ayudan a evitar errores accidentales al intentar cambiar valores que deben permanecer constantes.

Uso: Se utilizan en situaciones donde se necesita asegurar que un valor no cambie, como en cálculos matemáticos precisos, configuraciones estáticas del programa, o valores que se comparten entre diferentes partes del código.

Sintaxis: En (.k+), la sintaxis para declarar una constante es similar a la de declarar una variable, pero usando const en lugar de var.
